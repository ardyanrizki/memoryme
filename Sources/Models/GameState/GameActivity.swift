import Foundation

/**
 Represents different activities in a scene.
 */
enum GameActivity {
    /// Activity representing the scene opening.
    case opening
    
    /// Activity representing exploring the scene.
    case exploring
    
    /// Activity representing a dialog in the scene.
    case dialog
    
    /// Activity representing snapshots in the scene.
    case snapshots
    
    /// Activity representing a mini game in the scene.
    case minigame
}
